# 문자와 문자열
# 파이썬의 문자열(str)은 문자들의 시퀀스(순서 있는 집합)로, 리스트(배열)처럼 인덱싱과 슬라이싱이 가능하다.
# 단, 문자열은 불변(immutable) 객체라 값을 직접 바꿀 수는 없지만, 배열처럼 접근하는 것은 자유롭다.
# 대부분의 언어(C, C++, Java 등)에서도 문자열은 내부적으로 문자 배열로 취급된다.

word = input()  # 문자열 입력 (문자 배열처럼 인덱싱 가능)
i = int(input())  # 1-based 인덱스 입력

print(word[i-1])  # 파이썬 문자열도 리스트처럼 인덱싱 가능
