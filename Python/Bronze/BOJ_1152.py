# 단어의 개수 (BOJ 1152)
# 입력받은 문자열에서 단어의 개수를 출력

'''
# 직접 공백 개수를 세는 방식 (비추천)
# 아래와 같은 입력에서 오답이 발생할 수 있음:
# 1. 공백만 있는 경우: "    " → 실제 0, 코드 출력 1
# 2. 연속된 공백: "hello     world" → 실제 2, 코드 출력 6
# 3. 빈 문자열: "" → 실제 0, 코드 출력 1
str_count = input()
count = 0 
for i in range(1, len(str_count)-1):
    if str_count[i] == ' ':
        count += 1
print(count + 1)
'''

# 파이썬다운 안전한 풀이: split() 사용 (추천)
# split()은 연속된 공백, 맨 앞/뒤 공백, 빈 문자열 등 모든 예외를 자동으로 처리
str_count = input().split()
print(len(str_count))

# 정리:
# - 직접 공백 개수를 세는 방식은 예외 처리가 어렵고, 오답이 발생할 수 있음
# - split()을 사용하면 모든 케이스에서 안전하게 단어 개수를 셀 수 있음